# 将数据类型转换成字符串
int_to_str = str(11)
print(type(int_to_str), int_to_str)

float_to_str = str(13.14)
print(type(float_to_str), float_to_str)

# 将字符串转换成数字
str_to_int = int("123")
print(type(str_to_int), str_to_int)

str_to_float = float("13.14")
print(type(str_to_float), str_to_float)

# 将int转换为float
int_to_float = float(11)
print(type(int_to_float), int_to_float)

# 将float转换为int
float_to_int = int(13.14)
print(type(float_to_int), float_to_int)

