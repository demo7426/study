# 算数(数学)运算符
print("1 + 1 = ", 1 + 1)
print("2 - 1 = ", 2 - 1)
print("3 * 2 = ", 3 * 2)
print("9 / 2 = ", 9 / 2)
print("9 // 2 = ", 9 // 2)
print("5 % 2 = ", 5 % 2)
print("5 ** 2 = ", 5 ** 2)

# 赋值运算符
num = 1 + 2 * 3

# 复合赋值运算符
num += 1
print("num += 1: ", num)
num -= 1
print("num -= 1: ", num)
num *= 2
print("num *= 2: ", num)
num /= 2
print("num /= 2: ", num)
num //= 2
print("num //= 2: ", num)
num %= 2
print("num %= 2: ", num)
num = 2
num **= 3
print("num **= 2: ", num)
